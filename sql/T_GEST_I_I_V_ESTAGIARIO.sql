  INSERT
  INTO
    Estagiario
    (
      ID_PESSOA_ESTAGIARIO,
      ID_PESSOA_FUNCIONARIO,
      NB_FUNCIONARIO,
      TX_CEP,
      TX_ENDERECO,
      NB_NUMERO,
      TX_COMPLEMENTO,
      TX_BAIRRO,
      TX_AGENCIA,
      TX_CONTA_CORRENTE,
      TX_CONTATO,
      TX_EMAIL,
      NB_PCD
    )
    VALUES
    (
      :new.ID_PESSOA_ESTAGIARIO,
      :NEW.ID_PESSOA_FUNCIONARIO,
      :NEW.NB_FUNCIONARIO,
      :NEW.TX_CEP,
      :NEW.TX_ENDERECO,
      :NEW.NB_NUMERO,
      :NEW.TX_COMPLEMENTO,
      :NEW.TX_BAIRRO,
      :NEW.TX_AGENCIA,
      :NEW.TX_CONTA_CORRENTE,
      :NEW.TX_CONTATO,
      :NEW.TX_EMAIL,
      :NEW.NB_PCD
    );
END;
    (
      :new.ID_PESSOA_ESTAGIARIO,
      :new.tx_Nome,
      :new.cs_Tipo_Pessoa
    );
  -- INSERT TABLE Pessoa_Fisica
  INSERT
  INTO
    Pessoa_Fisica
    (
      ID_Pessoa,
      nb_cpf,
      nb_rg,
      cs_sexo,
      dt_atualizacao,
      dt_nascimento
    )
    VALUES
    (
      :new.ID_PESSOA_ESTAGIARIO,
      :new.nb_cpf,
      :new.nb_rg,
      :new.cs_sexo,
      sysdate,
      :new.dt_nascimento
    );
